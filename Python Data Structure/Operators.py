# Membership Operators
# in, not in
listA = [1, 2, 3, 4, 5, 6, 7, 8]
x = 1
y = 10
print("===================")
print (x in listA)
print (y in listA)
print (x not in listA)
print (y not in listA)

# Identity Operators
# is, is not
x = 20
y = 20
print("===================")
print (x is y)
print (x is not y)

